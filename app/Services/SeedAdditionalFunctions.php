    public function randomName($first) {
        $name;
        if($first)
        {
            $name = $this->firstname[rand ( 0 , count($this->firstname) -1)];
        }else{
            $name = $this->lastname[rand ( 0 , count($this->lastname) -1)];
        }

        return $name;
    }

    public function randomNumber($requiredLength = 8, $highestDigit = 9) {
                
        $sequence = '';
        for ($i = 0; $i < $requiredLength; ++$i) {
            $sequence .= mt_rand(0, $highestDigit);
        }
        return $this->numberPrefixes[array_rand($this->numberPrefixes)] . $sequence;
    }
    public function randomAmount()
